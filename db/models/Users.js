const mongoose = require('mongoose');

const Users = new mongoose.Schema({
  name: {
    type: String,
    required: true
  },
  email: {
    type: String,
    required: true
  },
  password: {
    type: String,
    required: true
  },
  phone: {
    type: String,
    required: true
  }
})

const accounts = mongoose.model("user", Users)
module.exports = accounts